package Udemy_Array_list;

public class Contacts {
    private String name ;
    private String phonenumber;

    public Contacts(String name, String phonenumber) {
        this.name = name;
        this.phonenumber = phonenumber;
    }

    public String getName() {
        return name;
    }

    public String getPhonenumber() {
        return phonenumber;
    }
}
